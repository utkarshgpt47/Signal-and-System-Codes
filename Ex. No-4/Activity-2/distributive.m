clc;
clear all;
n1= -10:20;
n2= 0:30;
n3= -5:10;
x1=n1;
x2=cos(0.1*pi*n2);
x3=1.2.^n3;
[y4,n4]=convm(x1,n1,x2,n2);
[y5,n5]=convm(x1,n1,x3,n3);
[y6,n6]=sigadd(y4,n4,y5,n5);
subplot(2,1,1);
stem(n6,y6);
xlabel('[n]');
ylabel('x[n]');
title('Distributive');
[y7,n7]=sigadd(x2,n2,x3,n3);
[y8,n8]=convm(x1,n1,y7,n7);
subplot(2,1,2);
stem(n8,y8);
xlabel('[n]');
ylabel('x[n]');